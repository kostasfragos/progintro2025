#include <cstdio>

int main() {
    double a = 1.2346;
    printf("%5.3lf", a);
}
// Το 5 καθορίζει τον ελάχιστο συνολικό αριθμό 
// των ψηφίων του αριθμού μαζί με την τελεία ('.')
// και το 3 καθορίζει την ακρίβεια του πραγματικού 
// αριθμού, δηλαδή πόσα δεκαδικά ψηφία θα έχει.
// Επίσης, γίνεται στογγυλοποίηση του αριθμού 
// με τέτοιο τρόπο ώστε αυτός να εκφραστεί
// με όσα δεκαδικά ψηφία υποδεικνύει ο αριθμός
// μετά την τελεία ('.').